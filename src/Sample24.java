
public class Sample24 {

	public static void main(String[]args) {
	
		//부정연산자 NOT(!)의 사용 예 ---->토글
		//toggle is like a switch.
		
		boolean play = true;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
		
		play = !play;
		System.out.println(play);
		
				
		
	}//main
	
}//end class
